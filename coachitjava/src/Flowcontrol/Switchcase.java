package Flowcontrol;

public class Switchcase {

	public static void main(String[] args) {
		int x=2;
		switch(x+2)
		
		{
		case 1:
			
			System.out.println("case1:value is 1:" +x);
		
		case 4:
			
			System.out.println("case2 value is 2:" +x);
			
		case 3:
			System.out.println("case3 value is 3:" +x);
			
		default:
			
		System.out.println("casedefault value:"+x);
		
		}

	}

}
