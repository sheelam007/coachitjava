package Flowcontrol;

public class Switchbreakcase {

	public static void main(String[] args) {
       int i=2;
       switch(i){
       case 1:
           System.out.println("case1:");
           break;
       case 2:
    	   System.out.println("case2:");
    	   break;
       case 3:
    		System.out.println("case 3:");
    		break;
       }
       }
       

	}


